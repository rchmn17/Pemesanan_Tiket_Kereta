    private void loadPanelHistory(){
        model.removeAll();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm");
        for (Pemesanan p : dao.getPesanans()) {
            if (p.getUserPembeli().getName().equals(Session.getUser().getName())){
                TemplateHistories panel = new TemplateHistories(p);
                String[] tanggal = p.getTanggal().split("-");
                panel.getTanggalLabel().setText("PEMESANAN " + tanggal[2] + "/" + tanggal[1] + "/" + tanggal[0]);
                panel.getJamBerangkatLabel().setText("Jam Keberangkatan : " + p.getJadwal().getWaktuKeberangkatan().format(formatter));
                panel.getJamKedatanganLabel().setText("Jam Kedatangan : " + p.getJadwal().getWaktuKedatangan().format(formatter));
                panel.getStasiunLabel().setText("Stasiun : " + p.getJadwal().getStasiunAwal().getNama() + " - " + p.getJadwal().getStasiunAkhir().getNama()+ " ("+ p.getJadwal().getStasiunAkhir().getKota()+")");
                panel.getKeretaLabel().setText("Kereta : " + p.getJadwal().getKereta().getNama() + "(" + p.getJadwal().getKereta().getTipeKereta() + ")");
                int hargaTotal=0;
                for (Tiket t : p.getItemOrder()) {
                    hargaTotal += p.getJadwal().getHarga();
                }
                panel.getHargaLabel().setText("Nominal : Rp." + hargaTotal);
                panel.loadPenumpang(p.getItemOrder());
                model.add(panel);
                model.add(Box.createRigidArea(new Dimension(0, 10)));
            }
        }
        model.revalidate();
        model.repaint();
    }

